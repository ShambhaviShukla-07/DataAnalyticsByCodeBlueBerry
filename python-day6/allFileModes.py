# r - read only mode...throws error if file does not exist
# w - write only mode...throws error if file does not exist...removes old entries
# a - append mode(write without deleting old entries)...creates new file if not created...does not delete old entries
# r+ - read and write mode...throws error if file does not exist...does not delete old entries
# w+ - read and write mode...creates new file if not created...removes old entries
# a+ - read and write mode(with append)...creates new file if not created...does not delete old entries